package cli

import "github.com/spf13/cobra"

var predictionAgentsCmd = &cobra.Command{
	Use:   "prediction-agents",
	Short: "View prediction agents",
	Long: `Browse prediction agents that generate crowd forecasts for prediction subjects.

Prediction agents produce AI-assisted prediction distributions and may be linked
to an autogenerated prediction.

Commands:
  list    List prediction agents
  show    Show prediction agent details`,
	Example: `  # List prediction agents
  xbe view prediction-agents list

  # Show a prediction agent
  xbe view prediction-agents show 123`,
}

func init() {
	viewCmd.AddCommand(predictionAgentsCmd)
}
